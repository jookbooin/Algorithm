/**
 * n가지 종류의 동전 ( 100 )
 * 가치의 합이 k     ( 10000 )
 * 
 * 사용한 동전의 구성이 같은데 순서만 다른 것은 같은 경우
 * 동전은 몇개라도 사용할 수 있다.
 * 
 * 1 2 5
 * 
 * 0 : 0
 * 1 : 1 
 * 2 : (11) (2)
 * 3 : (111) (12)
 * 4 : (1111) (112) (22) 
 * 5 : (11111) (1112) (122) (5)
 * 6 : (111111) (11112) (1122) (222) (15)
 * 7 : (1111111) (111112) (11122) (1222) (115) (25) 
 * 8 : (11111111) (1111112) (111122) (11222) (2222) (1115) (125)
 * 9 : (111111111) (11111112) (1111122) (111222) (12222) (11115) (1125) (225)
 * 10 : (1111111111) (111111112) (11111122) (1111222) (112222) (22222) (111115) (11125) (1225) (55)
 * 
 *
 * k원이 되기 위해서 ?
 * 
 * 점화식 
 * dp[n] = (dp[8-1],  dp[8-2], dp[8-5]
 *          dp[7],    dp[6],   dp[3]
 */
#include <bits/stdc++.h>

using namespace std;

int n,k,input;

int dp[10004];

int main(){
    cin >> n >> k;

    dp[0] = 1;
    for(int i =0; i<3; i++){
        cin >> input;
        for(int i = input; i<=k; i++){
            dp[i] += dp[i-input];
        }
    }

    cout << dp[k] << endl;
    return 0;
}