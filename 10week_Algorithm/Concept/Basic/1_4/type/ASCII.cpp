#include <bits/stdc++.h>
using namespace std;
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    char A = 'A';
    cout << "A : " << (int)A << '\n';
    char Z = 'Z';
    cout << "Z : " << (int)Z << '\n' << endl;

    char a = 'a';
    cout << "a : " << (int)a << '\n';
    char z = 'z';
    cout << "z : " << (int)z << '\n';

    return 0;
}
